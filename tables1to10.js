// Nested loops to print the multiplication table of numbers from 1 to 10
      // Outer loop for the first number (a)
      for (let i = 1; i <= 10; i++) {
        // Inner loop for the second number (b)
        for (let j = 1; j <= 10; j++) {
          console.log(i + " x " + j + " = " + i * j);
        }
      }
